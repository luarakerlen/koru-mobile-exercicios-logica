/**
 * Faça uma função que inicialize um número inteiro qualquer.
 * Como resultado, imprima seu antecessor e o seu sucessor.
 */

void main() {
  int numero = 45;
  int antecessor = numero - 1;
  int sucessor = numero + 1;

  print('O antecessor de $numero é $antecessor e o sucessor é $sucessor.');
}
